/**
 * ユーザーの質問をラップして送るヘルパー。
 */
export function buildUserTurn(question: string, hasImage: boolean) {
  if (hasImage) {
    return `添付した画像を確認し、システムプロンプトの指示(画像が添付されている場合のルール)に厳密に従って回答してください。\n\n補足コメント(あれば): ${question || "(なし)"}`;
  }
  return `次の数学の問題(または質問)に、システムプロンプトのフォーマットに厳密に従って答えてください。\n\n---\n${question}\n---`;
}
